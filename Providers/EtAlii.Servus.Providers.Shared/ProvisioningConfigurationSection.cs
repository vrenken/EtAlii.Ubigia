namespace EtAlii.Servus.Providers
{
    using System.Configuration;

    public class ProvisioningConfigurationSection : ConfigurationSection, IProvisioningConfiguration
    {
        [ConfigurationProperty("password", IsRequired = false)]
        public string Password 
        {
            get { return this["password"] as string; }
            set { this["password"] = value; }
        }

        [ConfigurationProperty("account", IsRequired = true)]
        public string Account
        {
            get
            {
                return this["account"] as string;
            }
        }

        [ConfigurationProperty("address", IsRequired = true)]
        public string Address
        {
            get
            {
                return this["address"] as string;
            }
        }
    }
}